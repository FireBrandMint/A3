    private void PesquisarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_PesquisarMouseClicked
        int[] resultado = Servidor.PesquisarProjetos(BarraPesquisa.getText().split(", "));
        
        if (resultado != null && resultado.length > 0)
        {
            var paginas = new ArrayList<Projeto>();
            
            for (int i = 0; i< resultado.length; ++i)
            {
                int IDprojeto = resultado[i];
                
                for (int o = 0; o< Projetos.length; ++o)
                {
                    if (Projetos[o].ID == IDprojeto)
                    {
                        paginas.add(Projetos[o]);
                        break;
                    }
                }
            }
            
            Paginas = paginas.toArray(new Projeto[0]);
            
            MudarPagina(0);
        }
    }//GEN-LAST:event_PesquisarMouseClicked

