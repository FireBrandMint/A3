    private void MudarPagina (int pagina)
    {
        PaginaPresente = pagina;
        
        Projeto p = Paginas[pagina];
        
        Nome.setText(p.Nome);
        Descricao.setText(p.Descricao);
        Autor.setText(p.UsuarioProprietario);
        
        ContadorPagina.setText(Integer.toString(pagina + 1) + "/" + Integer.toString(Paginas.length));
        
        ResetarUI();
    }
    
    private void ResetarUI ()
    {
        Salvar.setVisible(false);
        Cancelar.setVisible(false);
        Criar.setVisible(false);
        Descartar.setVisible(false);
        Proximo.setVisible(true);
        Anterior.setVisible(true);
        IndAutor.setVisible(true);
        Autor.setVisible(true);
        ContadorPagina.setVisible(true);
        CriarProjeto.setVisible(true);
        Nome.setEditable(false);
        Descricao.setEditable(false);
        bRequisitos.setVisible(true);
        Pesquisar.setVisible(true);
        BarraPesquisa.setVisible(true);
        
        boolean temPermissao = Servidor.MeuUsuario.ID == Paginas[PaginaPresente].IDProprietario || Servidor.MeuUsuario.Permissao > 0;
        
        Editar.setVisible(temPermissao);
        Usuarios.setVisible(Servidor.MeuUsuario.Permissao > 0);
        
        Deletar.setVisible(temPermissao);
    }

