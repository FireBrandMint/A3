    private void SalvarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_SalvarMouseClicked
        Projeto projetoAtual = Paginas[PaginaPresente];
        
        Projeto p = Servidor.EditarProjeto(projetoAtual.ID, Nome.getText(), Descricao.getText());
        
        if (p != null)
        {
            projetoAtual.Nome = p.Nome;
            projetoAtual.Descricao = p.Descricao;
        }
            
        MudarPagina(PaginaPresente);
    }//GEN-LAST:event_SalvarMouseClicked

